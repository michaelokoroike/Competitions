#Bitwise operators are used to compare (binary) numbers.

# & = AND; sets each bit to 1 if both bits are one (merge)
# | = OR; sets each bit to 1 if one of two bits are one (merge)
# ^ = XOR; sets each bit to 1 if only one of two bits is 1 (merge)
# ~ = NOT; invert bits
# << = Zero fill left shift; bitwise operators are used to compare (binary) numbers
# >> = Signed right shift; shift right by pushing copies of the leftmost bit in from the left, and let the rightmost bits fall off
