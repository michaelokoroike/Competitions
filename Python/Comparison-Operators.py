#Comparison operators are used to compare two values:

x = 1
y = 1

print(x == y) #are x and y equal to each other? Would print True

print(x != y) #are x and y not equal to each other? Would print False

print(x > y) #is x greater than y? Would print False

print(x < y) #is x less than y? Would print False

print(x >= y) #is x greater than or equal to y? Would print True

print(x <= y) #is x less than or equal to y? Would print True
